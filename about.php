DropMail is a full fledged mail service that implements a filesystem for an email client to implement a web application accessed to authorise the user with an account to perform all mail operations:

*Compose an email.
*Send an email
*Receive an email
*Saving an email in the form of draft
*Showing the detail of the received mail
*Saving the email ids in contact list
*Secured by password
*Recovery of the password